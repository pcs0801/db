CREATE TABLE PERSISTENT_LOGINS(
    USERNAME VARCHAR2(64) NOT NULL,
    SERIES VARCHAR2(64) NOT NULL,
    TOKEN VARCHAR2(64) NOT NULL,
    LAST_USED DATE NOT NULL,
    PRIMARY KEY (SERIES)
);